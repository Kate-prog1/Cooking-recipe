document.getElementById("searchButton").addEventListener("click", function() {
    var searchInput = document.getElementById("searchInput").value.toLowerCase();
    var searchResults = document.getElementById("searchResults");

    // Очищаем результаты предыдущего поиска
    searchResults.innerHTML = "";

    // Проверяем введенное слово
    if (searchInput === "банан") {
        // Добавляем первый результат - Творожные сырники с бананом
        var dish1 = document.createElement("div");
        dish1.classList.add("dish");
        dish1.innerHTML = `
            <img src="https://samors.ru/wp-content/uploads/2021/12/Tvorozhnye-syrniki-s-bananom.jpg" alt="Творожные сырники с бананом">
            <h3>Творожные сырники с бананом и рисовой мукой</h3>
            <p><strong>Описание:</strong> Сырники с бананом, приготовленные по этому рецепту, – это один из способов разнообразить полезную творожную выпечку. Отличный рецепт на завтрак для всей семьи, который обогатит ваш здоровый рацион.</p>
            <p><strong>Ингредиенты:</strong> Творог 5 % – 180 г. Банан – 1 шт. Яйцо – 1 шт. Мука рисовая – 2 ст. л. Подсластитель. Соль – ⅓ ч. л. Масло оливковое – 1 ч. л.</p>
            <p><strong>Приготовление:</strong> <br>
            1- Разминаем вилкой нежирный творог. Добавляем яйцо, муку, соль, подсластитель по вкусу. Снова все перетираем вилкой до однородной кремообразной массы. <br>
            2- Банан режем кружочками – он должен быть спелым и сладким. <br>
            3- Рабочую поверхность присыпаем рисовой мукой. Выкладываем с помощью большой ложки порцию теста на один сырник. Сверху кладем кусочек банана. Подтягиваем края лепешки вверх, заворачиваем начинку в тесто. <br>
            4- Придаем сырникам форму при помощи большого ножа. Выкладываем на разогретую сковороду, готовим на среднем огне без крышки в течение 2–3 минут до румяного цвета. <br>
            5- Подаем с пылу с жару с кусочками темного шоколада, ягодным сиропом или греческим йогуртом.</p>`;
        searchResults.appendChild(dish1);

        // Добавляем второй результат - Банановые панкейки на овсяной муке
        var dish2 = document.createElement("div");
        dish2.classList.add("dish");
        dish2.innerHTML = `
            <img src="https://samors.ru/wp-content/uploads/2021/12/Bananovye-pankejki-na-ovsjanoj-muke.jpg" alt="Банановые панкейки на овсяной муке">
            <h3>Банановые панкейки на овсяной муке</h3>
            <p><strong>Описание:</strong> Пышные, в меру сладкие и совсем не жирные овсяные панкейки с бананом. Они пекутся без сахара, но для дополнительной сладости вы можете добавить мед или сахарозаменитель. Кстати, тесто из этого рецепта можно использовать для вафель. Рецепт особенно оценят те, кто не употребляет глютен и лактозу.</p>
            <p><strong>Ингредиенты:</strong> Мука овсяная – 100 г. Молоко – 100 мл. Разрыхлитель – 1 ч. л. Бананы – 250 г. Яйцо – 1 шт. Корица по желанию. Соль.</p>
            <p><strong>Приготовление:</strong> <br>
            1- В глубокую миску разбиваем яйцо. Добавляем молоко, щепотку соли, размешиваем венчиком. Кстати, если использовать миксер, панкейки получатся еще более пышными. <br>
            2- Разрыхлитель смешиваем с овсяной мукой. Частями вводим муку в яичную смесь. Готовое тесто должно получиться достаточно густым, примерно как на оладушки. <br>
            3- Бананы взбиваем при помощи измельчителя в нежное, жидкое пюре. Добавляем банановое пюре в тесто. Хорошо размешиваем венчиком. <br>
            4- Разогреваем сковороду с хорошим антипригарным покрытием. Жарим панкейки без масла, на небольшом огне, по две минуты с каждой стороны.</p>`;
        searchResults.appendChild(dish2);
    } else {
        // Если введенное слово не "банан", вы можете добавить здесь обработку других запросов или вывести сообщение о том, что ничего не найдено.
        var noResults = document.createElement("p");
        noResults.textContent = "Ничего не найдено.";
        searchResults.appendChild(noResults);
    }
});
